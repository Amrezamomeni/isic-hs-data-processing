df["عنوان Division"] = df["کد Division"].apply(
    lambda x: division_titles.get(x, f"تعریف نشده ({x})") if x else ""
)

df["حرف بخش"] = df["کد Division"].apply(
    lambda x: division_to_section.get(x, "")
)

df["نام بخش"] = df["حرف بخش"].apply(
    lambda x: section_titles.get(x, "")
)

